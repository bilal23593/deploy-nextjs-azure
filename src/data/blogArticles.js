const blogCollectionCounts = blogArticles.reduce((map, article) => {
  const key = article.collection || article.category;
  map.set(key, (map.get(key) || 0) + 1);
  return map;
}, new Map());

export const blogCollectionList = [
  ...Object.entries(blogCollectionDefinitions).map(([label, definition]) => ({
    ...definition,
    label,
    slug: definition.slug || taxonomyLabelToSlug(label),
    count: blogCollectionCounts.get(label) || 0,
  })),
  ...Array.from(blogCollectionCounts.entries())
    .filter(([label]) => !(label in blogCollectionDefinitions))
    .map(([label, count]) => ({
      label,
      slug: taxonomyLabelToSlug(label),
      count,
      title: `${label} Articles | Cube Cake Studiios`,
      description: "Advanced strategy articles for teams improving product and service clarity.",
      heroSummary: "A curated topic hub for this article group.",
      introParagraphs: [
        "This collection groups related articles from the Cube Cake Studiios blog.",
      ],
      keywords: [label.toLowerCase()],
      image: "/images/blog/design-business-growth.svg",
    })),
].filter((collection) => collection.count > 0);

